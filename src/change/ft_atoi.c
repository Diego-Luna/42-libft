int	ft_atoi(const char *str)
{
	int	i;
	int	result;
	int	positive;

	i = 0;
	positive = 1;
	result = 0;
	while (str[i] != '\0' && str[i] == 32)
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			positive *= -1;
		}
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		result = (str[i] - 48) + (result * 10);
		i++;
	}
	return (result * positive);
}
